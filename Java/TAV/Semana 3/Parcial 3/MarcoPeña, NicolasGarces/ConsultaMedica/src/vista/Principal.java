    private void jButtonGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonGuardarMouseClicked
        Paciente paciente = new Paciente();
        Registro registro = new Registro();
        if (!jTextFieldRut.getText().isEmpty()) {
            paciente.setRut(jTextFieldRut.getText());
        } else {
            JOptionPane.showMessageDialog(this, "El rut no puede estar vacio", "atencion", JOptionPane.WARNING_MESSAGE);
        }
        if (!jTextFieldNombre.getText().isEmpty()) {
            paciente.setNombreCompleto(jTextFieldNombre.getText());
        } else {
            JOptionPane.showMessageDialog(this, "El nombre no puede estar vacio", "atencion", JOptionPane.WARNING_MESSAGE);
        }

        if (jRadioButtonFemenino1.isSelected()) {

            paciente.setGenero("F");
        }
        if (jRadioButtonMasculino.isSelected()) {

            paciente.setGenero("M");
        }
        if (!jTextFieldEdad.getText().isEmpty()) {
            try {
                paciente.setEdad(Integer.parseInt(jTextFieldEdad.getText()));
            } catch (NumberFormatException error) {
                JOptionPane.showMessageDialog(this, "Edad no validad", "Atención", JOptionPane.WARNING_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "La edad no puede estar vacio", "atencion", JOptionPane.WARNING_MESSAGE);
        }

        if (!jTextFieldDireccion.getText().isEmpty()) {
            paciente.setDireccion(jTextFieldDireccion.getText());

        } else {
            JOptionPane.showMessageDialog(this, "La direccion no puede estar vacio", "atencion", JOptionPane.WARNING_MESSAGE);
        }
        paciente.setCiudad(jComboBoxCiudad.getSelectedItem().toString());
        if (jRadioButtonSi.isSelected()) {
            paciente.setIsapre("S");
        }
        if (jRadioButtonNo.isSelected()) {
            paciente.setIsapre("N");
        }
        if (jCheckBoxDondante.isSelected()) {
            paciente.setDonante(true);
        } else {
            paciente.setDonante(false);
        }

        if (paciente.getEdad() != 0) {
            int rowAffected = registro.agrega(paciente);

            if (rowAffected == 1) {
                JOptionPane.showMessageDialog(this, "Paciente registrado exitosamente", "Atencion", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Paciente no fue registrado ", "Atencion", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Paciente no fue registrado ", "Atencion", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonGuardarMouseClicked

