    private void mostrarTabla(JTable tabla) {
        Object[] titulo = {"Rut", "Nombre Completo", "Genero", "Edad", "Direccion", "Ciudad", "Isapre", "Donante"};
        Registro registro = new Registro();
        DefaultTableModel dfm = new DefaultTableModel();
        dfm.setColumnIdentifiers(titulo);
        ArrayList<Paciente> pacientes = registro.buscarTodos();
        for (Paciente p : pacientes) {
            dfm.addRow(new Object[]{p.getRut(), p.getNombreCompleto(), p.getGenero(), p.getEdad(), p.getDireccion(), p.getCiudad(), p.getIsapre(), p.isDonante()});
        }
        tabla.setModel(dfm);
    }

    private void mostrarTablaPorRut(JTable tabla) {
        Object[] titulo = {"Rut", "Nombre Completo", "Genero", "Edad", "Direccion", "Ciudad", "Isapre", "Donante"};
        Registro registro = new Registro();
        Paciente p = new Paciente();
        DefaultTableModel dfm = new DefaultTableModel();
        dfm.setColumnIdentifiers(titulo);
        p = registro.buscarPorRut(jTextFieldBuscaRut.getText());

        dfm.addRow(new Object[]{p.getRut(), p.getNombreCompleto(), p.getGenero(), p.getEdad(), p.getDireccion(), p.getCiudad(), p.getIsapre(), p.isDonante()});

        tabla.setModel(dfm);
    }

