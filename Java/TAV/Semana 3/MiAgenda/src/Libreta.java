    private void mostrarDatosTabla(JTable tabla) {
        Object[] titulo = {"Correo", "Telefono", "Nombre", "Apellido"};
        ContactoADO cliente_ado = new ContactoADO();
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.setColumnIdentifiers(titulo);
        ArrayList<Contacto> contactos = cliente_ado.findAll();
        for (Contacto c : contactos) 
            dtm.addRow(new Object[]{c.getCorreo(), c.getTelefono(), c.getNombre(), c.getApellido()});
        tabla.setModel(dtm);
    }
    
    
    
    
    
