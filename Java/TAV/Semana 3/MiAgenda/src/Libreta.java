    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        Contacto contacto = new Contacto();
        ContactoADO contacto_ado = new ContactoADO();
        contacto.setCorreo(jTextCorreo.getText());
        contacto.setTelefono(jTextTelefono.getText());
        contacto.setNombre(jTextNombre.getText());
        contacto.setApellido(jTextApellido.getText());
        int rowAffected=contacto_ado.create(contacto);
        if (rowAffected==1)
            JOptionPane.showMessageDialog(this, "Contacto creado exitosamente", "Atencion", JOptionPane.INFORMATION_MESSAGE);
        else
            JOptionPane.showMessageDialog(this, "Contacto no fue creado ", "Atencion", JOptionPane.ERROR_MESSAGE);
        this.mostrarDatosTabla(jTable2);
        
    }//GEN-LAST:event_jButton1MouseClicked

